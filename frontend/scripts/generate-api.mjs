const __dirname = dirname(fileURLToPath(import.meta.url))
const frontendDir = join(__dirname, '..')
const outDir = join(frontendDir, 'src', 'api', 'generated')
const outPath = join(outDir, 'schema.ts')
const rawPath = join(outDir, 'schema.raw.ts')
const localSnapshot = join(__dirname, 'openapi.snapshot.json')

function generateFrom(source, label) {
  const bin = join(frontendDir, 'node_modules', 'openapi-typescript', 'bin', 'cli.js')
  const args = [bin, source, '-o', rawPath]
  console.log(`[api:generate] generating from ${label}`)
  const res = spawnSync(process.execPath, args, { stdio: 'inherit', shell: false })
  return res.status === 0
}

async function loadOpenApi(source) {
  if (source.startsWith('http://') || source.startsWith('https://')) {
    const res = await fetch(source)
    if (!res.ok) {
      throw new Error(`OpenAPI fetch failed with status ${res.status}`)
    }
    return res.json()
  }

  return JSON.parse(readFileSync(source, 'utf8'))
}

