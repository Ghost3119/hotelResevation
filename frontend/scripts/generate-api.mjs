function generateFromSnapshot() {
  if (!existsSync(localSnapshot)) return false
  const ok = generateFrom(localSnapshot, 'snapshot')
  if (ok) {
    writeCompatibilityWrapper(JSON.parse(readFileSync(localSnapshot, 'utf8')))
  }
  return ok
}

async function generateFromLiveBackend(source) {
  const openApi = await loadOpenApi(source)
  const ok = generateFrom(source, 'live backend')
  if (ok) {
    writeCompatibilityWrapper(openApi)
  }
  return ok
}

async function backendReachable(url) {
  try {
    const controller = new AbortController()
    const timeout = setTimeout(() => controller.abort(), 4000)
    const res = await fetch(url, { signal: controller.signal })
    clearTimeout(timeout)
    return res.ok
  } catch {
    return false
  }
}

if (!existsSync(outDir)) mkdirSync(outDir, { recursive: true })

let ok = false
const liveSchema = 'http://localhost:8080/api/openapi.json'

if (await backendReachable(liveSchema)) {
  ok = await generateFromLiveBackend(liveSchema)
}

if (!ok) {
  console.log('[api:generate] Backend not reachable. Falling back to local snapshot.')
  ok = generateFromSnapshot()
}

if (!ok) {
  console.log('[api:generate] No snapshot found. Keeping existing src/api/generated/schema.ts (manually authored).')
  if (!existsSync(outPath)) {
    writeFileSync(outPath, '// placeholder - generated by npm run api:generate\n')
  }
}

console.log(`[api:generate] done -> ${outPath}`)
