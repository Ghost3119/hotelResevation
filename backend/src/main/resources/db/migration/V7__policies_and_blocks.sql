-- Modifica:
--   * rate_plans:    anade FK cancellation_policy_id -> cancellation_policies
--     ON DELETE SET NULL.
--   * reservations:  anade FK cancellation_policy_id -> cancellation_policies
--     ON DELETE SET NULL.
--
-- Notas:
--   * room_blocks previene solapamiento por habitacion con EXCLUDE USING gist
--     sobre (room_id, overlap_range), patron identico a reservation_rooms (V1).
--     Requiere btree_gist (ya instalado en V1; se repite por seguridad).
--   * La exclusion GiST de room_blocks opera sobre TODAS las filas (incluidas
--     las released_at NOT NULL). El backend debe DELETE o actualizar las filas
--     liberadas antes de crear un bloqueo solapante, o ajustar la logica.
