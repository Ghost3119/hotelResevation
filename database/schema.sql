-- -----------------------------------------------------------------------------
-- refresh_tokens  (V3__refresh_tokens.sql)
-- Persistencia de refresh tokens para el flujo de auth con rotacion.
--   * token_hash: SHA-256 del token (nunca el token original).
--   * jti:        JWT ID unico del token emitido.
--   * revoked_at: NULL = activo; fecha = revocado (rotacion o logout).
--   * replaced_by_jti: jti del token que lo reemplazo al rotar.
--   * device_info: User-Agent / info de sesion (opcional).
--   * CHECK (expires_at > created_at): el token nace valido.
-- Deteccion de reutilizacion: si se presenta un token ya revocado, el backend
-- revoca la cadena y devuelve 401. Ver docs/database.md seccion 9.
-- -----------------------------------------------------------------------------
CREATE TABLE refresh_tokens (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id         BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    token_hash      TEXT NOT NULL UNIQUE,
    jti             TEXT NOT NULL UNIQUE,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
    expires_at      TIMESTAMPTZ NOT NULL,
    revoked_at      TIMESTAMPTZ,
    replaced_by_jti TEXT,
    device_info     TEXT,
    CONSTRAINT refresh_tokens_check_expiry CHECK (expires_at > created_at)
);

CREATE INDEX idx_refresh_tokens_user_id    ON refresh_tokens (user_id);
CREATE INDEX idx_refresh_tokens_jti        ON refresh_tokens (jti);
CREATE INDEX idx_refresh_tokens_token_hash ON refresh_tokens (token_hash);
CREATE INDEX idx_refresh_tokens_expires_at ON refresh_tokens (expires_at);

