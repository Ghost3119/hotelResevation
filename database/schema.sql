-- reservation_nightly_rates  (V6, ER-9)  -- snapshot inmutable por noche
-- -----------------------------------------------------------------------------
CREATE TABLE reservation_nightly_rates (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    reservation_id      BIGINT NOT NULL REFERENCES reservations(id) ON DELETE CASCADE,
    rate_plan_id        BIGINT NOT NULL REFERENCES rate_plans(id) ON DELETE RESTRICT,
    night_date          DATE NOT NULL,
    base_rate           NUMERIC(12,2) NOT NULL CHECK (base_rate >= 0),
    extra_person_charge NUMERIC(12,2) NOT NULL DEFAULT 0 CHECK (extra_person_charge >= 0),
    discount_amount     NUMERIC(12,2) NOT NULL DEFAULT 0 CHECK (discount_amount >= 0),
    taxes_amount        NUMERIC(12,2) NOT NULL DEFAULT 0 CHECK (taxes_amount >= 0),
    fees_amount         NUMERIC(12,2) NOT NULL DEFAULT 0 CHECK (fees_amount >= 0),
    total               NUMERIC(12,2) NOT NULL,
    included            BOOLEAN NOT NULL DEFAULT TRUE,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT reservation_nightly_rates_total_chk CHECK (
        total = base_rate + extra_person_charge - discount_amount + taxes_amount + fees_amount
    )
);

